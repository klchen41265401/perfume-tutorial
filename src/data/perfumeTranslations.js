/**
 * Translate a role string from Chinese to English
 * @param {string} role Chinese role text
 * @returns {string} English translation
 */
export function translateRole(role) {
  if (!role) return ''

  // 1. Direct match
  if (ROLE_DIRECT[role]) return ROLE_DIRECT[role]

  // 2. Try decomposition – match longest material prefix
  let bestMat = '', bestMatEn = ''
  for (const [zh, en] of Object.entries(ROLE_MATERIALS)) {
    if (role.startsWith(zh) && zh.length > bestMat.length) {
      bestMat = zh
      bestMatEn = en
    }
  }

  if (bestMat) {
    const suffix = role.slice(bestMat.length)
    if (!suffix) return bestMatEn
    // try to match suffix
    if (ROLE_QUALITIES[suffix]) return `${bestMatEn} ${ROLE_QUALITIES[suffix]}`
    // try suffix as a second material
    if (ROLE_MATERIALS[suffix]) return `${bestMatEn}–${ROLE_MATERIALS[suffix]}`
    // try suffix as direct
    if (ROLE_DIRECT[suffix]) return `${bestMatEn} ${ROLE_DIRECT[suffix]}`
    // partial suffix: try matching suffix prefix
    let bestQ = '', bestQEn = ''
    for (const [qz, qe] of Object.entries(ROLE_QUALITIES)) {
      if (suffix.startsWith(qz) && qz.length > bestQ.length) {
        bestQ = qz
        bestQEn = qe
      }
    }
    if (bestQ) {
      const remainder = suffix.slice(bestQ.length)
      const remEn = ROLE_QUALITIES[remainder] || ''
      return `${bestMatEn} ${bestQEn}${remEn ? ' ' + remEn : ''}`
    }
    return `${bestMatEn} — ${suffix}`
  }

  // 3. Try as pure quality
  if (ROLE_QUALITIES[role]) return ROLE_QUALITIES[role].charAt(0).toUpperCase() + ROLE_QUALITIES[role].slice(1)

  // 4. Fallback
  return role
}

